#import "UIColor+BackgroundColor.h"

@implementation UIColor (BackgroundColor)

+ (UIColor *)backgroundColor
{
    return [UIColor colorWithRed:29/255.0 green:29/255.0 blue:29/255.0 alpha:1.0];
}

+ (UIColor *)cellColor
{
    return [UIColor colorWithRed:60/255.0 green:90/255.0 blue:120/255.0 alpha:1.0];
}

+ (UIColor *)fontColor
{
    return [UIColor colorWithRed:158/255.0 green:153/255.0 blue:1 alpha:1.0];
}

@end
